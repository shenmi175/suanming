export function stableHash(input: string) {
  let hash = 2166136261;
  for (let index = 0; index < input.length; index += 1) {
    hash ^= input.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

export function getIChingHexagram(seedInput: string) {
  const seed = stableHash(seedInput);
  const index = seed % 64;
  const [name, theme] = hexagrams[index];

  return {
    id: "iching-hexagram",
    number: index + 1,
    name,
    theme,
    seed,
    confidence: "medium" as const,
    notes: ["根据用户输入生成稳定 seed，同样输入会得到同样卦象；此处以象取意，不作定论。"],
  };
}
